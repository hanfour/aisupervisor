const COLOR_MAP = {
  '1': 'hair',
  '2': 'skin',
  '3': 'shirt',
  '4': 'pants',
  '5': 'shoes',
  '6': 'eye',
}

const AVATAR_TO_CHAR = {
  'coder': 'coder',
  'hacker': 'hacker',
  'designer': 'designer',
  'analyst': 'analyst',
  'architect': 'architect',
  'devops': 'devops',
  // fallback mapping by index
}
const CHARACTER_NAMES = Object.keys(PALETTES)

export function getCharacterType(worker, index) {
  if (worker.avatar && AVATAR_TO_CHAR[worker.avatar]) return AVATAR_TO_CHAR[worker.avatar]
  return CHARACTER_NAMES[index % CHARACTER_NAMES.length]
}

export function renderSpriteToCanvas(ctx, rows, palette, x, y, scale) {
  for (let row = 0; row < rows.length; row++) {
    const line = rows[row]
    for (let col = 0; col < line.length; col++) {
      const ch = line[col]
      if (ch === '0') continue
      let color
      if (palette && COLOR_MAP[ch]) {
        color = palette[COLOR_MAP[ch]]
      } else if (ch === '6') {
        color = '#111'
      } else if (FURNITURE_PALETTE[ch]) {
        color = FURNITURE_PALETTE[ch]
      } else {
        continue
      }
      ctx.fillStyle = color
      ctx.fillRect(x + col * scale, y + row * scale, scale, scale)
    }
  }
}

// Prerender all animation frames of a character to offscreen canvases
export function prerenderCharacter(charType) {
  const palette = PALETTES[charType]
  if (!palette) return null
  const cache = {}
  const scale = 1
  const size = 16 * scale
  for (const [state, frames] of Object.entries(CHARACTER_FRAMES)) {
    cache[state] = frames.map(rows => {
      const offscreen = document.createElement('canvas')
      offscreen.width = size
      offscreen.height = size
      const ctx = offscreen.getContext('2d')
      renderSpriteToCanvas(ctx, rows, palette, 0, 0, scale)
      return offscreen
    })
  }
  return cache
}

export function prerenderFurniture(name) {
  const rows = FURNITURE_SPRITES[name]
  if (!rows) return null
  const scale = 1
  const size = 16 * scale
  const offscreen = document.createElement('canvas')
  offscreen.width = size
  offscreen.height = size
  const ctx = offscreen.getContext('2d')
  renderSpriteToCanvas(ctx, rows, null, 0, 0, scale)
  return offscreen
}

